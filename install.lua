--[[
  PWR Reactor Control System - Installer
  Run this on each computer to install the required files.
  
  Usage:
    wget https://raw.githubusercontent.com/brambora69123/OC-PWR-System/main/install.lua install.lua
    install.lua
]]

local component = require("component")
local fs        = require("filesystem")
local shell     = require("shell")

local GITHUB = "https://raw.githubusercontent.com/brambora69123/OC-PWR-System/main/"

-- {github_path, local_path}
local REACTOR_FILES = {
  {"lib/protocol.lua",    "/lib/protocol.lua"},
  {"lib/reactor_ctrl.lua","/lib/reactor_ctrl.lua"},
  {"lib/colors.lua",      "/lib/colors.lua"},
  {"reactor_server.lua",  "/home/reactor_server.lua"},
}

local CLIENT_FILES = {
  {"lib/protocol.lua",    "/lib/protocol.lua"},
  {"lib/reactor_ctrl.lua","/lib/reactor_ctrl.lua"},
  {"lib/colors.lua",      "/lib/colors.lua"},
  {"lib/gpu2.lua",        "/lib/gpu2.lua"},
  {"control_client.lua",  "/home/control_client.lua"},
  {"setup_screens.lua",   "/home/setup_screens.lua"},
}

local function detectType()
  if component.isAvailable("ntm_pwr_control") then return "reactor" end
  if component.isAvailable("gpu") and component.gpu.getDepth() >= 8 then return "client" end
  return "unknown"
end

local function ensureDir(path)
  local dir = fs.path(path)
  if dir and not fs.exists(dir) then fs.makeDirectory(dir) end
end

local function installFiles(files)
  local ok, fail = 0, 0
  for _, pair in ipairs(files) do
    local urlPath, localPath = pair[1], pair[2]
    io.write("  " .. localPath .. " ... ")
    ensureDir(localPath)
    local tmpFile = "/tmp/_pwr_install_dl"
    local url = GITHUB .. urlPath
    local okWget, err = shell.execute('wget -fq "' .. url .. '" "' .. tmpFile .. '"')
    if okWget and fs.exists(tmpFile) then
      shell.execute('mv "' .. tmpFile .. '" "' .. localPath .. '"')
      io.write("OK\n")
      ok = ok + 1
    else
      io.write("FAILED: " .. tostring(err) .. "\n")
      fail = fail + 1
    end
  end
  return ok, fail
end

local function setupAutorun(compType)
  local script = compType == "reactor" and "reactor_server" or "control_client"
  local path = "/boot/autorun.lua"
  ensureDir(path)
  local f = io.open(path, "w")
  if not f then print("WARNING: Could not create " .. path); return false end
  f:write('-- Auto-generated by PWR Reactor Control System installer\n')
  f:write('local ok, err = pcall(dofile, "/home/' .. script .. '.lua")\n')
  f:write('if not ok then print("[PWR] " .. tostring(err)) end\n')
  f:close()
  print("Autorun: " .. path .. " -> " .. script)
  return true
end

local function main()
  print("========================================")
  print("  PWR Reactor Control System Installer")
  print("========================================\n")

  local compType = detectType()
  print("Detected: " .. compType)

  if compType == "unknown" then
    print("\nSelect:\n  [1] Reactor PC\n  [2] Control Room PC")
    io.write("Choice: ")
    local c = io.read()
    if c == "1" then compType = "reactor"
    elseif c == "2" then compType = "client"
    else print("Invalid."); return end
  end

  print()
  local files = compType == "reactor" and REACTOR_FILES or CLIENT_FILES
  local ok, fail = installFiles(files)
  print("\nDone. " .. ok .. " OK, " .. fail .. " failed.")

  print()
  io.write("Install autorun on boot? [Y/n]: ")
  local c = io.read()
  if not c or c:lower() ~= "n" then setupAutorun(compType) end

  print()
  if compType == "reactor" then
    print("Run: reactor_server")
  else
    print("Run: setup_screens (once), then: control_client")
  end
  print("To disable autorun: rm /boot/autorun.lua")
end

main()
